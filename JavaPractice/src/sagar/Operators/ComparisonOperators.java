package Operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        int v = 10;
        int s = 7;

        System.out.println("m == n: " + (v == s)); // false
        System.out.println("m != n: " + (v != s)); // true
        System.out.println("m > n: " + (v > s));   // true
        System.out.println("m < n: " + (v < s));   // false
        System.out.println("m >= n: " + (v >= s)); // true
        System.out.println("m <= n: " + (v <= s)); // false
    }
}


