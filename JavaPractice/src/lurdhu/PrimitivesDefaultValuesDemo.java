package src.lurdhu;

public class PrimitivesDefaultValuesDemo {
    static int age;
    static long phoneNumber;
    static String name;
    static double salary;
    static boolean isAGradeStudent;
    static char grade;
    public static void main(String[] args) {

        System.out.println("int default value : " + age);
        System.out.println("long default value : " + phoneNumber);
        System.out.println("String default value : " + name);
        System.out.println("double default value : " + salary);
        System.out.println("boolean default value : " + isAGradeStudent);
        System.out.println("char default value : " + grade);
    }
}
